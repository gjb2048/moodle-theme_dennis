defined('MOODLE_INTERNAL') || die();

user_preference_allow_ajax_update('drawer-open-nav', PARAM_ALPHA);
require_once($CFG->libdir . '/behat/lib.php');

if (isloggedin()) {
    $navdraweropen = (get_user_preferences('drawer-open-nav', 'true') == 'true');
} else {
    $navdraweropen = false;
}
$extraclasses = [];
if ($navdraweropen) {
    $extraclasses[] = 'drawer-open-left';
}
$bodyattributes = $OUTPUT->body_attributes($extraclasses);
$blocksprehtml = $OUTPUT->dennisblocks('side-pre');
$haspreblocks = strpos($blocksprehtml, 'data-block=') !== false;
$blocksfooterhtml = $OUTPUT->dennisblocks('footer', 3);
$buildregionmainsettings = !$PAGE->include_region_main_settings_in_header_actions();
// If the settings menu will be included in the header then don't add it here.
$regionmainsettingsmenu = $buildregionmainsettings ? $OUTPUT->region_main_settings_menu() : false;
$serverdatetime = strftime('%d/%m/%Y %H:%M:%S');
$PAGE->requires->js_call_amd('theme_dennis/serverdatetime', 'serverdatetime',
    array('data' => array('currentdatetime' => $serverdatetime)));
$templatecontext = [
    'sitename' => format_string($SITE->shortname, true, ['context' => context_course::instance(SITEID), "escape" => false]),
    'output' => $OUTPUT,
    'sidepreblocks' => $blocksprehtml,
    'haspreblocks' => $haspreblocks,
    'footerblocks' => $blocksfooterhtml,
    'bodyattributes' => $bodyattributes,
    'navdraweropen' => $navdraweropen,
    'regionmainsettingsmenu' => $regionmainsettingsmenu,
    'hasregionmainsettingsmenu' => !empty($regionmainsettingsmenu),
    'serverdatetime' => $serverdatetime
];

$nav = $PAGE->flatnav;
$templatecontext['flatnavigation'] = $nav;
$templatecontext['firstcollectionlabel'] = $nav->get_collectionlabel();
echo $OUTPUT->render_from_template('theme_dennis/course', $templatecontext);
